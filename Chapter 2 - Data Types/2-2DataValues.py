# There are two ways of specifying data values in python
# Literal - Literal values entered in.
# Variable - Representations of other values (entered or declared elsewhere)
# Rules for varibles are
# 1. They can only contain: Upper Case, Lower Case, Digits, and Underscore
# 2. They are case-sensitive
# 3. They must begin with a letter or digit, not underscore
# 4. Names that begin with an underscore are treated specially (chapter 9)
# 5. They cannot be one of pythons reserved words : https://docs.python.org/2.5/ref/keywords.html

# = means assignment of a value not equal on both sides like in math.

# Variables are names not places. Variables are never copies of data, just attaches a name to a reference of data.